                <div class="ProjectDescription">
                    "The NASA Student Launch, or SL, is a research-based, competitive, experiential exploration activity. SL has partnered with the Centennial Challenges to provide relevant, cost-effective research and development of rocket propulsion systems. This project offers multiple challenges reaching a broad audience of middle and high schools, colleges and universities, and non-academic teams across the nation. Participation involves an eight-month commitment to design, construct, and fly payloads and vehicle components and/or to design, construct, and operate ground support equipment. The teams launch the experiments on high-powered rockets and share the research results, which will be used in future design and development of NASA projects." - <a href="http://www.nasa.gov/audience/forstudents/studentlaunch/home/" target="_blank" class="ProjectDescriptionLink">NASA Student Launch Website</a>
